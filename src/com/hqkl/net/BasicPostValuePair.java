package com.hqkl.net;

import org.apache.http.NameValuePair;

public class BasicPostValuePair implements NameValuePair{
	private String name,value;
	public BasicPostValuePair(String name,String value){
		this.name = name;
		this.value = value;
	}
	@Override
	public String getName() {
		return this.name;
	}
	public void setName(String name) {
		this.name = name;
	}
	@Override
	public String getValue() {
		return this.value;
	}
	public void setValue(String value) {
		this.value = value;
	}
}
